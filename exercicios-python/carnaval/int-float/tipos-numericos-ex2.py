#Faça um programa que receba a base e altura de um triângulo e imprima a área dele.


base = float(input("Digite aqui a base do triangulo: "))

altura = float(input("Digite aqui a altura do triangulo: "))

area = (base * altura) / 2

print(f"A área para um triangulo de base {base} e altura {altura} é de {area}cm²")